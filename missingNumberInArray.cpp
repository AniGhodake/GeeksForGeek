class Solution{
  public:
    int missingNumber(vector<int>& array, int n) {
        sort(array.begin(),array.end());
        int ans;
        for(int i = 0; i< n-1; i++){
            if(i+1 != array[i]){
                ans = i+1;
                break;
            }
            else if(i+1 == array[i]){
                ans = i+2;
            }
        }
        return ans;
    }
};


